$(document).ready(function() {
  $(".foodheader").click(function() {
    $(".pp").show();
    $(".foodheader").hide();
  });
  $(".pp").click(function() {
    $(".foodheader").show();
    $(".pp").hide();
  });
  $(".header3").click(function(){
    $(".ppp").show();
    $(".header3").hide();
  });
  $(".ppp").click(function(){
    $(".header3").show();
    $(".ppp").hide();
  });
  $(".header6").click(function(){
    $(".pppp").show();
    $(".header6").hide();
  });
  $(".pppp").click(function(){
    $(".header6").show();
    $(".pppp").hide();
  });
});
